    public void setActiveButton(TimeType timeType) {
        // timeType:2 - 7 days, 3 - 30 days, 4 - 90 days
        Button[] btns = { btn7Days, btn30Days, btn90Days};
        for (int i = 0; i < btns.length; i++) {
            if (i == timeType.getValue() - 2) {
                btns[i].setBackground(new java.awt.Color(0, 123, 255));
                btns[i].setForeground(new java.awt.Color(255, 255, 255));
            } else {
                btns[i].setBackground(new java.awt.Color(214,217,223));
                btns[i].setForeground(new java.awt.Color(0, 0, 0));
            }
        }
    }



