    private void customTable() {
        addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mousePressed(java.awt.event.MouseEvent e) {
                tbl.getTbl().clearSelection();
            }
        });
        String cols[] = {"Mã hóa đơn", "Ngày đặt", "Ngày trả", "Doanh thu", "Loại phòng"};
        DefaultTableModel model = new DefaultTableModel(cols, 0);
        tbl.getTbl().setModel(model);

        var header = tbl.getTbl().getTableHeader();
        Combobox<String> cmbCheckInDate = new Combobox<>(new String[]{"Tất cả", "6h-12h", "12h-18h", "18h-0h", "0h-6h"});
        Combobox<String> cmbRevenue = new Combobox<>(new String[]{"Tất cả", "Tăng dần", "Giảm dần"});
        Combobox<String> cmbRoomType = new Combobox<>(new String[]{"Tất cả", "Phòng Đơn", "Phòng đôi"});

        var colModel = tbl.getTbl().getColumnModel();
        TableCellRenderer defaultRenderer = header.getDefaultRenderer();

        colModel.getColumn(1).setHeaderRenderer((tbl, value, isSelected, hasFocus, row, col) -> {
            Component comp = defaultRenderer.getTableCellRendererComponent(tbl, value, isSelected, hasFocus, row, col);
            if (comp instanceof JLabel lbl) {
                String text = "Ngày đặt                           \u25BC";
                lbl.setText(text);
                lbl.setHorizontalAlignment(SwingConstants.LEFT);
            }
            return comp;
        });

        colModel.getColumn(3).setHeaderRenderer((tbl, value, isSelected, hasFocus, row, col) -> {
            Component comp = defaultRenderer.getTableCellRendererComponent(tbl, value, isSelected, hasFocus, row, col);
            if (comp instanceof JLabel lbl) {
                String text = "Doanh thu                             \u25BC";
                lbl.setText(text);
                lbl.setHorizontalAlignment(SwingConstants.LEFT);
            }
            return comp;
        });

        colModel.getColumn(4).setHeaderRenderer((tbl, value, isSelected, hasFocus, row, col) -> {
            Component comp = defaultRenderer.getTableCellRendererComponent(tbl, value, isSelected, hasFocus, row, col);
            if (comp instanceof JLabel lbl) {
                String text = "Loại phòng                             \u25BC";
                lbl.setText(text);
                lbl.setHorizontalAlignment(SwingConstants.LEFT);
            }
            return comp;
        });

        header.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int col = header.columnAtPoint(e.getPoint());
                switch (col) {
                    case 1:
                        showCombo(cmbCheckInDate, col);
                        break;
                    case 3:
                        showCombo(cmbRevenue, col);
                        break;
                    case 4:
                        showCombo(cmbRoomType, col);
                        break;
                }
            }
        });

    }

