    @SuppressWarnings("unchecked")
    private void showCombo(Combobox<String> combo, int col) {
        Rectangle rect = tbl.getTbl().getTableHeader().getHeaderRect(col);
        combo.setBounds(rect);
        combo.setVisible(true);
        tbl.getTbl().getTableHeader().add(combo);
        combo.showPopup();

        combo.addFocusListener(new java.awt.event.FocusAdapter() {
            @Override
            public void focusLost(java.awt.event.FocusEvent fe) {
                combo.setVisible(false);
                tbl.getTbl().getTableHeader().remove(combo);
            }
        });
    }

