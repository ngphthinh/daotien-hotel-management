    /**
     * Thiết lập step đang active (0 → 4)
     * @param step step cần active
     */
    public void setActiveStep(int step) {
        JLabel[] labels = { lbl1, lbl2, lbl3,};
        CircleNumber[] circles = {
                circleNumber1,
                circleNumber2,
                circleNumber3,
        };

        Color activeColor = new Color(77, 134, 168);
        Color inactiveColor = new Color(105, 105, 105);

        if (step < 0 || step >= labels.length) {
            return;
        }

        // Reset toàn bộ
        for (int i = 0; i < labels.length; i++) {
            labels[i].setForeground(inactiveColor);
            circles[i].setActive(false);
        }



        for (int i = 0; i <= step; i++) {
            labels[i].setForeground(activeColor);
            circles[i].setActive(true);
        }
    }


